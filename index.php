            <h2>General Information</h2>
            <p>
                I'm a 17 year old high school student living in Greece, with a passion for computer science and language.
                I've been programming for nearly 8 years, focusing on writing efficient and expandable software in C++, and in particular, free and open-source,
                utilizing copyleft licensing (GNU General Public License v3).
                In addition, I have presented one of my most recent projects, PhysicsCPP, at the 13th Panhellenic/International Conference "ICT in Education".
                I'm currently working on a custom programming language called Linc (Linc Is Not C) with the goal of offering an alternative to languages like C,
                C++ and Rust for system level programming. The language has already seen a great amount of progress, with support for static typing, functions, loops,
                string manipulation, preprocessor definitions and more... I believe it to be my biggest and most interesting project so far.
            </p>

            <h2>Personal Interests</h2>
            <p>
                One could confidently say I'm quite the music enjoyer. I've been playing the piano for a few years and have been studying music theory and
                18th century voice leading (though my favorite music genre is jazz, funnily enough). Music can offer a relaxing contrast to programming,
                as well as helping minimize its routine-intensive and repetitive nature. I'm also fascinated by the intricacies of language and love to study it,
                whether it's morphology and phonology of existing natural languages, or of constructed languages (like Toki Pona). In fact, I've been, slowly
                but surely, making my own, aiming for simplicity and grammatical consistency above all, not focusing on naturalism. 
            </p>
            <p>
                From time to time, I find myself endulging in video-game activities, particularly those wtih a high creativity aspect, as well as those in which
                the player can directly reflect on their progression. When offline, I sometimes enjoy a good game of chess (though I don't play over-the-board
                exclusively), as I appreciate strategizing my every move, slowly but surely improving with each game. Moreover, I have an interest for
                puzzles, even though I'm not the best at it. Impressively enough, I managed to solve a Rubik's cube in just under 56 seconds (using the CFOP method).
                I'm not very sporty, but I love to play table tennis whenever I get the chance to (I'm actually not the worst). 
            </p>
            
